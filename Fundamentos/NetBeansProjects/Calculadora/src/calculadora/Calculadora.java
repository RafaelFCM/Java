    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        int num = Integer.parseInt(jSpinner1.getValue().toString());
        int res = num % 2;
        lblRes.setText(Integer.toString(res));
        double el = Math.pow(num, 3);
        lblElev.setText(Double.toString(el));
        double rq = Math.sqrt(num);
        lblRaizQ.setText(String.format("%.2f",rq));
        double rc = Math.cbrt(num);
        lblRaizC.setText(String.format("%.2f",rc));
        int abs = Math.abs(num);
        lblAbs.setText(Integer.toString(abs));
        pan.setVisible(true);
    }//GEN-LAST:event_btnCalcActionPerformed

